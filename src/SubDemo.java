public class SubDemo {
    public static void main(String[] args){

        int x = 100;
        int y = 200;
        int sub = x - y;
        int mul = x * y;
        float div = x / y;
        System.out.println("Subtraction of x and y: " + sub);
        System.out.println("Multiplication of x and y: " + mul);
        System.out.println("Division of x by y: " + div);
    }
}
