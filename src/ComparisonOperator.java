public class ComparisonOperator {
    public static void main(String[] args){
        int x = 10;
        int y = 20;
        int a = 30;
        int b = 15;

        System.out.println(x == y);
        System.out.println(x == a);
        System.out.println(x == b);
        System.out.println(a == y);
        System.out.println(x != y);
        System.out.println(x > a);
        System.out.println(x < b);
        System.out.println(a >= y);
    }
}
